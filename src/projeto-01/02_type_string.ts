//TIPO STRING

// Declarando a variável com seu tipo já atribuindo um valor
let nome1: string = 'Romulo';
console.log(`nome1: ${nome1}!`);

// Declarando a variável com seu tipo sem atribuir um valor diretamente
let nome2: string;
// Atribuindo um valor à variável
nome2 = 'Rosa';
console.log(`nome2: ${nome2}!`);