// TYPE ALIAS vs INTERFACES

// PONTOS EM COMUM ENTRE ALIAS E INTERFACES
// Ambos são utilizados para definir:
// - Novos tipos de dados
// - Regras de um tipo de dado
// - Contrato/padrão de um tipo de dado
// - Tipo de dados que pode ter vários valores, tipos de dados, métodos, construtores e herança



// DIFERENÇAS ENTRE ALIAS E INTERFACES

// ALIASES usar para :
// - Simplificar o código e evitar repetições.
// - Renomear tipos
// - Renomear tipos sem restringir os tipos de dados.
// - Restringir tipos de dados e construir contratos.
// - Criar novos tipos a partir de outros tipos e adicionar novos métodos/propriedades, construtores e herança.

// INTERFACES usar para:
// - Estruturar o código, organizar e documentar os tipos de dados.
// - Restringir os tipos de dados, construir contratos, regras e herança
// - Criar novos tipos a partir de outros tipos, métodos/propriedades, novos construtores, herança,
//   restrições de tipos, valores, métodos, construtores, herança e herança múltipla.


// RECOMENDAÇÕES
// - Num projeto em andamento seguir o que já estiver em uso
// - Lembrar que num mesmo projeto não é necessário usar ambos, mas podem ser úteis para
//   estruturar o código, organizar e documentar o tipo de dados


